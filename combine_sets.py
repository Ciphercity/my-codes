# combining sets
# union operator |; used to combine sets to make one with elements in either
# intersection operator &; gets item,s only in both 
# defference operator -; gets items only in the first but not both
# symetric difference ^; used to get items in either sets but not both
# run the following program
# 
# 
# 
first ={1,3,5,7,9,}
second = {1,2,3,4,5,6,7,}
print(first | second)
print(first & second)
print(first - second)
print(first ^ first)